package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		// Assignment Operators
		//   = ,+= ,-= ,*= ,/= ,%=   
		
		int a = 5;
		
		a%=6;// a = a%6
		System.out.println(a);
		
		a/=3;// a = a/3;
		System.out.println(a);
		
		a*=3;
		System.out.println(a);
		
		a-=44;// a = a-44;
		System.out.println(a);
		
		a+=3; // a = a+3;
		System.out.println(a);

	}

}
