package operators;

public class LogicalOperators {

	public static void main(String[] args) {
		// Logical operators
		//   && , ||
		
		boolean f1 = true;
		boolean f2 = false;
		
		boolean f4 = f1||f2;
		System.out.println(f4);
		
		boolean f3 = f1&&f2;
		System.out.println(f3);
		
		boolean f5 = f1&f2;
		System.out.println(f5);
		boolean f6 = f1|f2;
		System.out.println(f6);
		boolean f7 = f1^f2;
		System.out.println(f7);
		
		

	}

}
